<template>
  <v-container>
    <v-dialog v-model="isGameOn" class="mx-auto" max-width="500">
      <v-card
        :color="game.gameState == GameState.Won ? 'success' : 'error'"
        tile
        class="pa-5 text-center"
      >
        <h3>
          You've
          {{ game.gameState == GameState.Won ? "Won! ðŸ¥³" : "Lost... ðŸ˜­" }}
        </h3>
        <v-divider class="my-5" />

        <v-card-text class="text-h5">
          The word was: <strong>{{ game.secretWord }}</strong>
        </v-card-text>
        <v-divider class="my-5" />
        <v-btn variant="outlined" @click="game.startNewGame()">
          <v-icon size="large" class="mr-2"> mdi-restart </v-icon> Restart Game
        </v-btn>
      </v-card>
    </v-dialog>
    <GameBoardGuess
      v-for="(guess, i) of game.guesses"
      :key="i"
      :guess="guess"
    />
    <div class="d-flex justify-center ma-3">
      <v-btn
        v-if="game.gameState !== GameState.Playing"
        @click="isGameOn = true"
        color="primary"
        text="RESULTS"
        height="50px"
        width="200px"
      />
    </div>

    <Keyboard />

    <WordList v-model="showWordsList" />

    <v-bottom-navigation>
      <v-btn value="recent" @click="showWordsList = true">
        <v-icon>mdi-history</v-icon>

        <span>Word List</span>
      </v-btn>
    </v-bottom-navigation>
  </v-container>
</template>

<script setup lang="ts">
import { Game, GameState } from "../scripts/game";
const game: Game = reactive(new Game());
const showWordsList = ref(false);
const isGameOn = ref(false);

provide("GAME", game);

onMounted(() => {
  window.addEventListener("keyup", onKeyup);
});

onUnmounted(() => {
  window.removeEventListener("keyup", onKeyup);
});

function onKeyup(event: KeyboardEvent) {
  if (showWordsList.value) {
    return;
  }

  if (event.key === "Enter") {
    game.submitGuess();
  } else if (event.key == "Backspace") {
    game.removeLastLetter();
  } else if (event.key.match(/[A-z]/) && event.key.length === 1) {
    game.addLetter(event.key.toUpperCase());
  }
}

watch(game, () => {
  if (game.gameState !== GameState.Playing) {
    isGameOn.value = true;
  } else {
    isGameOn.value = false;
  }
});
</script>
